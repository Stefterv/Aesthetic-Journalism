		<h1 class="level3">A 
			<span class="level2">Skewed</span> 
			Reality
		</h1>
			<h3 class="level3">Local Reality</h3>
			<p>
				<span class="level3">Alfredo Jaar</span>
				’s the sound of silence is an installation that 
				<span class="level2">dedicates 300 cubic meters and 8 minutes of time to </span>
				<span class="level3">one image. This image</span>
				, made by Kevin Carter, 
				<span class="level2">has put the </span>
				<span class="level3">famine</span>
				<span class="level2">in </span>
				<span class="level3">Rwanda</span>
				<span class="level2">on the </span>
				<span class="level3">headlines of the world</span>
				<span class="level2">’s papers.</span> 
				He wants to tell the story behind the image, 
				<span class="level3">the story that is</span> 
				<span class="level2">not</span> 
				<span class="level3">covered by the media</span>
				<span class="level2">. To give perspective</span> 
				on how our journalistic system works. 
				<span class="level2">Jaar is trying to show </span>
				<span class="level3">the context</span> 
				around the creation of this image and what 
				<span class="level3">side of the story</span> 
				<span class="level2">got all of the </span>
				<span class="level3">coverage</span> 
				<span class="level2">and what side did not get any</span> 
				<span class="level3">attention</span> 
				<span class="level2">at all. In his work, Alfredo Jaar</span> 
				often 
				<span class="level3">challenges the viewer</span> 
				<span class="level2">to look </span>
				<span class="level3">differently</span> 
				at the way the 
				<span class="level3">media</span> 
				presents the world. 
				<span class="level2">The same is true for </span>
				<span class="level3">The Sound Of Silence</span>
				<span class="level2">, in which Alfredo Jaar wants</span> 
				you 
				<span class="level2">to</span> 
				think about how we as a society 
				<span class="level3">look at the african continent</span> 
				and it’s inhabitants. 
			</p>
			<p>
				<span class="level2">One of the things that triggered my interest</span> 
				in the work 
				<span class="level2">is how it tries to show </span>
				<span class="level3">the difference in</span> 
				<span class="level2">reality that we </span>
				<span class="level3">experience</span> 
				when seeing this shocking photograph 
				<span class="level3">in comparison to</span> 
				<span class="level2">what the actual reality is which led to the creation of </span>
				<span class="level3">this image.</span> 
				<span class="level2">Where does this difference come from? How is it that images lead us to</span> 
				believe 
				<span class="level2">things that might not </span>
				<span class="level3">be true?</span>
			</p>
		
			<h3 class="level3">Orientalism</h3>
			<p>
				<span class="level2">To start our </span>
				<span class="level3">search</span> 
				in where our reality comes from, 
				<span class="level2">we have to go back to the early days of </span>
				<span class="level3">colonialism.</span> 
				In
				<span class="level2"> On </span>
				<span class="level3">Orientalism</span>
				, Edward Said 
				<span class="level3">explains</span> 
				<span class="level2">why we look at </span>
				<span class="level3">the East</span> 
				<span class="level2">the way we do.</span>
			</p>
			<p>
				It all started 
				<span class="level3">when</span> 
				<span class="level2">early</span> 
				<span class="level3">western society</span> 
				<span class="level2">first</span> 
				<span class="level3">came into contact with</span> 
				the exotic cultures in 
				<span class="level3">the East.</span> 
				<span class="level2">Because these cultures where so</span> 
				drastically different from 
				<span class="level2">ours, to try and understand them the science of Orientalism was established. </span>
			</p>
			<p>
				<span class="level3">Orientalism is</span> 
				<span class="level2">the study of the orient by the western culture. Because of </span>

				<span class="level3">our</span> 
				technical and cultural advantage
				<span class="level2">, we saw the </span>
				<span class="level3">Orient</span> 
				<span class="level2">as our younger brother who we needed </span>
				to protect and 
				<span class="level3">help to grow</span> 
				<span class="level2">independent. This idea justified colonisation</span> 
				for the european countries	
				<span class="level2">, as we believed </span>
				<span class="level3">it was our duty to civilise</span> 
				<span class="level2">the uncivilised parts of </span>
				<span class="level3">the world.</span> 
			</p>
			<p>
				<span class="level2">On the other hand, </span>
				<span class="level3">western civilisation</span> 
				<span class="level2">used the orient to </span>
				<span class="level3">define itself.</span> 
				Using the concept of them and us, 
				<span class="level2">a view of </span>
				<span class="level3">the East</span> 
				<span class="level2">as an </span>
				<span class="level3">uncivilised</span> 
				<span class="level2">and underdeveloped</span> 
				<span class="level3">part of the world</span>
				<span class="level2">, our </span>
				<span class="level3">self-image</span> 
				<span class="level2">as a </span>
				<span class="level3">superior</span> 
				race developed by specifying everything that we weren't as something the orient was. Edward Said explained this phenomenon: “On the one hand there are Westerners, and on the other there are Arab-Orientals; the former are (in no particular order) 
				<span class="level3">rational, peaceful, liberal, logical, capable of holding real values, without natural suspicion</span>
				<span class="level2">; the latter are </span>
				<span class="level3">none of these things</span>
				<span class="level2">”</span>
			</p>
			<p>
				Centuries later, these same values and beliefs still hold. 
				<span class="level2">A lot has </span>
				<span class="level3">changed</span>
				, colonialism has almost disappeared, 
				<span class="level2">most of the </span>
				<span class="level3">colonised countries</span> 
				<span class="level2">are</span> 
				<span class="level3">now independent states.</span> 
				<span class="level2">But we still look </span>
				the same way 
				<span class="level2">towards the Orient. A subconscious thought of</span> 
				<span class="level3">terrorism</span> 
				every time we hear the word Islam is not uncommon. Africa is still associated with 
				<span class="level3">death and disease.</span> 
				Why do 
				<span class="level2">these ideas of western superiority and dominance still </span>
				<span class="level3">persist in our modern day?</span>
			</p>
		
			<h3 class="level3">A limited scope</h3>
			<p>
				First 
				<span class="level2">we have to look at how the orient is covered in our </span>
				<span class="level3">media.</span> 
				<span class="level2">David </span>
				<span class="level3">Natharius wrote about</span> 
				our media in the general sense in his article about the dominance of the visual image in the 
				<span class="level3">media.</span> 
				<span class="level2">We have moved from a </span>
				<span class="level3">linear</span> 
				<span class="level2">and</span> 
				<span class="level3">written</span> 
				<span class="level2">literacy</span> 
				<span class="level3">to visual</span> 
				<span class="level2">and</span> 
				<span class="level3">holistic literacy.</span> 
				<span class="level2">This change is clearly visible in his example of our reaction on the terrorist attack on the </span>
				<span class="level3">world trade centre.</span>  
				<span class="level2">“On September 11, 2001, we were ushered into a world of terrible visual immediacy. For days after this tragedy, we were exposed, over and over again, to </span>
				<span class="level3">televised images of the planes crashing</span> 
				<span class="level2">into the towers, </span>
				<span class="level3">the towers burning</span>
				<span class="level2">, and then the towers collapsing. I was in my car going to school and heard the news on the radio. My first act, on reaching the office, was to turn on </span>
				the television and then phone my wife to turn on 
				<span class="level2">the television. Drivers on freeways looked for the first off-ramp to find a restaurant or service station or motel office that had a television set. If we had any doubt about the power of the visual image on civilisation, that doubt was forever erased. We have </span>
				<span class="level3">moved from a world of literacy to a world of visuality.</span> 
				A<span class="level2">nd as media literacy students and scholars, it is this new world of visuality that offers us our greatest arena of exploration.” Although </span>
				<span class="level3">this example</span> 
				<span class="level2">is not necessarily about </span>
				<span class="level3">the orient, it</span> 
				<span class="level2">perfectly illustrates how </span>
				<span class="level3">dependent we have become on visual</span> 
				<span class="level2">media to create our own </span>
				<span class="level3">reality.</span>
			</p>
			<p>
				<span class="level3">In Aesthetic Journalism Alfredo Cramerotti mentions</span> 
				three thinkers that explain 
				<span class="level3">why</span> 
				in our current culture, 
				<span class="level3">the dominant fiction is created through visual imagery.</span> 
				<span class="level2">We see visual imagery often as </span>
				<span class="level3">truth</span> 
				<span class="level2">because we assume that our visual perception is </span>
				<span class="level3">the most accurate</span> 
				<span class="level2">of our </span>
				<span class="level3">senses.</span> 
				<span class="level2">But we </span>
				<span class="level3">forget</span> 
				<span class="level2">that we </span>
				<span class="level3">have</span> 
				<span class="level2">been</span> 
				<span class="level3">taught</span> 
				<span class="level2">to</span> 
				see, just like we have been taught to 
				<span class="level3">read</span> 
				<span class="level2">and</span> 
				<span class="level3">write.</span> 
				<span class="level2">This means that the reality that we </span>
				<span class="level3">construct</span> 
				<span class="level2">from our</span> 
				<span class="level3">visually dominated media</span> 
				<span class="level2">feels more </span>
				<span class="level3">real</span> 
				than written media would, 
				<span class="level2">but actually is just as </span>
				<span class="level3">inaccurate.</span> 
				<span class="level2">In the sound of silence this inaccuracy is </span>
				<span class="level3">conceptualised by showing</span> 
				<span class="level2">that there is more </span>
				<span class="level3">context behind</span> 
				<span class="level2">Kevin Carter’s </span>
				<span class="level3">picture</span> 
				than what is visible. 
				<span class="level2">A lot of what we get out of the photograph is </span>
				<span class="level3">based on our pre-existing knowledge</span> 
				<span class="level2">about the Orient, and in this case </span>
				<span class="level3">Africa.</span> 
				<span class="level2">We do this to fill in </span>
				<span class="level3">the blanks</span> 
				<span class="level2">that the photograph </span>
				<span class="level3">cannot show</span> 
				<span class="level2">us. Because we </span>
				<span class="level3">miss</span> 
				a lot of information about 
				<span class="level2">the context, people got angry at Carter for not helping the girl, when Jaar fills in the blanks as to why Carter didn’t help the little girl, </span>
				<span class="level3">we might</span> 
				suddenly understand the situation and 
				<span class="level3">agree with Carter’s decision.</span>
			</p>
			<h3>The Feedback Loop</h3>
			<p>
				<span class="level2">Because of this inaccuracy in reporting </span>
				<span class="level3">we subconsciously see</span> 
				<span class="level2">our own beliefs and interpretations </span>
				<span class="level3">as truths.</span> 
				<span class="level2">Effectively creating </span>
				<span class="level3">a feedback system</span> 
				in which the preconceived notions about the orient are constantly 
				<span class="level3">reinforced by</span> 
				<span class="level2">the</span> 
				<span class="level3">images that we see</span> 
				about it.
			</p>
			<p>
				<span class="level2">But the inaccuracy in visual media is not the only </span>
				<span class="level3">reason our knowledge about the orient</span> 
				<span class="level2">has stayed so </span>
				<span class="level3">limited. Storytelling is</span> 
				<span class="level2">another</span> 
				<span class="level3">large component of</span> 
				<span class="level2">journalism. Journalists</span> 
				use it all the time to put the news 
				<span class="level2">into</span> 
				<span class="level3">logical and manageable chunks. </span>
				<span class="level2">The problem with this approach is that </span>
				<span class="level3">everything</span> 
				t<span class="level2">hat doesn't fit into a simple narrative</span> 
				either does not make it in to the news or when it does it is 
				<span class="level3">mostly ignored.</span> 
				<span class="level2">John Green exemplifies this </span>
				<span class="level3">by comparing</span> 
				<span class="level2">it to our desire for stories to have </span>
				<span class="level3">a hero and a villain. “We</span> 
				<span class="level2">do</span> 
				<span class="level3">allow a little bit of grey</span> 
				<span class="level2">on both sides but most stories have to be black and white </span>
				<span class="level3">to make it into the dominant fiction.”</span> 
				<span class="level2">Our view of the orient as an inferior still holds </span>
				after all this time because 
				<span class="level3">the stories that challenge this view</span> 
				<span class="level2">often do not fit into a simple narrative. Of course our own story does not fit into this narrative either, but because it </span>
				<span class="level3">is much closer to us</span> 
				<span class="level2">and less mediated the same inaccuracies </span>
				<span class="level3">do not occur.</span>
			</p>
		
			<h3 class="level3">How to break the loop</h3>
			<p class="level1">The sound of silence and other works from Alfredo Jaar, effectively show how Aesthetic Journalism can be used to try and challenge our perception of the orient. They try to overcome the limitations created by journalism by showing the systems that create them. </p>
