<p id="titel">Alfredo Jaar’s portal to reality</p>
	<p id="auteur"><i>Kevin ten Thij</i></p>

<div id="broodtekst">

Een klein afrikaans meisje, 

<span class="cycle" level-1="kwetsbaar" level-2="lichtgeraakt" level-3="teer" level-4="<a href=/?page=reality>TEST2</a>" >kwetsbaar</span> 
	
en dicht bij haar einde maar nog zo jong, een aangrijpend beeld, een flits. Het beeld wordt afgekapt en ik wordt terug in de stoel der realiteit geworpen. Terwijl mijn ogen herstellen begin ik de tekst weer op te pakken waarmee het werk begonnen is. Alfredo Jaar’s Sound of Silence is een werk dat 
	
<span class="cycle" level-1="context" level-2="setting" level-3="perspectief" level-4="<a href=/?page=reality>TEST2</a>" >context</span> 
	
geeft en de situatie van fotojournalist Kevin Carter uitlegt. Toch bevat het werk maar één beeld. Een beeld dat geen enkele mogelijkheid krijgt zich te vestigen in mijn hoofd, omdat het onderbroken wordt met een oogverblindende flits waarmee het mij in de 
	
<span class="cycle" level-1="positie" level-2="stellingname" level-3="toestand" level-4="<a href=/?page=reality>TEST2</a>" >positie</span> 
	
van het meisje op de foto plaatst. Jaar lijkt zich erg bewust van het effect dat een beeld op de mens kan hebben. Hij houdt zich ook in eerdere werken bezig met de realiteit die journalistiek meebrengt en hier lijkt het specifiek te gaan om de realiteit die dit beeld representeert. Maar doet het dat wel? De foto is een vastgelegd moment in onze 

<span class="cycle" level-1="realiteit" level-2="werkelijkheid" level-3="<a href=/?page=discussie class=relation id=test>wezenlijkheid</a>" >realiteit</span>, 
	
maar het is een fractie van een seconde, uitgesneden uit onze tijdlijn. Het is een fractie van een landschap, uitgesneden uit onze wereld. </context> En een fractie van een conflict, uitgesneden uit onze geschiedenis. Mijn punt is: dit beeld is niet echt meer een documentatie van een realiteit, maar vormt zijn eigen realiteit. Alle ophef over deze ene foto, omschreven in het werk, laat zien dat het 

<span class="cycle" level-1="publiek" level-2="gehoor" level-3="gezelschap" level-4="<a href=/?page=reality>TEST2</a>" >publiek</span> 
	
alleen dit beeld ziet, en hier een compleet eigen waardeoordeel aan hangt. Het publiek heeft geen idee van de situatie waarin deze foto genomen is en het 
	
<span class="cycle" level-1="beeld" level-2="symbool" level-3="overzicht" level-4="<a href=/?page=reality>TEST2</a>" >beeld</span> 
	
speelt een rol op zich, het vormt een eigen hoofdstuk in onze geschiedenis omdat het zijn eigen waarde krijgt.<br>
<br>
	
Baudrillard is een van de vele filosofen die zich bezig houden met de connectie tussen de realiteit en 
	
<span class="cycle" level-1="fictie" level-2="fantasie" level-3="verdichting" level-4="<a href=/?page=reality>TEST2</a>" >fictie</span>
	
, en hoe fictie vaak leidt tot een nieuwe realiteit. Baudrillard stelt 4 fasen. <i>„Dit zouden dan de opeenvolgende fasen van het beeld zijn:<br>
	<ul>
het is de weerspiegeling van een fundamentele realiteit<br>
het maskeert en 
		
<span class="cycle" level-1="pervereert" level-2="vervormt" level-3="<a href=/?page=reality>TEST2</a>" >pervereert</span> 
		
	
een fundamentele realiteit<br>
het maskeert de afwezigheid van een fundamentele realiteit<br>
het heeft geen relatie met welke realiteit dan ook: het is zuiver een 

<span class="simulacrum" level-1="archetype" level-2="impersonatie" level-3="vertegenwoordiging" level-4="<a href=/?page=reality>TEST2</a>" >simulacrum</span> 
	
van zichzelf.” <sup>1</sup></i>
</ul>
	
Het beeld van Kevin Carter bevindt zich in de derde fase van het beeld. Eerst is het een weerspiegeling geweest van een 
	
<span class="cycle" level-1="fundamentele" level-2="essentiele" level-3="constitutionele" level-4="<a href=/?page=reality>TEST2</a>" >fundamentele</span> 
	
realiteit. Een fotoreportage van een conflict in zuid Afrika waarbij een lugubere en confronterende realiteit is vastgelegd. Vervolgens is dit beeld als enige realiteit beschouwd in de westerse wereld en gaat het zonder context en uitleg een eigen 
	
<span class="cycle" level-1="leven" level-2="existentie" level-3="teren" level-4="<a href=/?page=reality>TEST2</a>" >leven</span> 
	
leiden. Hiermee maskeert het de rest van de realiteit van het beeld en perverteert het het beeld dat het westen van de 
	
<span class="cycle" level-1="situatie" level-2="toestand" level-3="omstandigheid" level-4="<a href=/?page=reality>TEST2</a>" >situatie</span> 
	
heeft. De derde fase bestaat hier uit het idee dat het beeld een accurate weergave is van de situatie en dus geen verdere realiteit behoeft, het geeft de indruk dat dit de realiteit is en maskeert dus de afwezigheid hiervan. <br>
<br>
	
Een belangrijke vergelijking is ook die van Jacques Lacan die een triade stelt van „the imaginary”, „the symbolic” en „the real”. „The 

<span class="cycle" level-1="imaginary" level-2="fictional" level-3="hypothetical" level-4="<a href=/?page=reality>TEST2</a>" >imaginary</span> 

order” is het stadium in de menselijke ontwikkeling waarin de mens zich als op zichzelf staand individu gaat zien, los van de ouders. Dit gebeurt door middel van „the sense of 
	
<span class="cycle" level-1="self" level-2="autogenous" level-3="subjective" level-4="<a href=/?page=reality>TEST2</a>" >self</span>” 
	
welke mede gegenereerd door het zien van de weerspiegeling van een persoon zijn eigen verschijning. <i>„When it sees itself in the mirror, however, it does see itself as a whole, but it is only an image of the whole. The other in the mirror is Imaginary. The child will now identify itself with the image. This is called a dual kind of relation.” <sup>2</sup></i>  „The symbolic” is de fase waarin de mens 
	
<span class="cycle" level-1="betekenis" level-2="inhoud" level-3="belang" level-4="<a href=/?page=reality>TEST2</a>" >betekenis</span> 
	
gaat geven aan dingen door middel van associatieve symbolisering; een fase waardoor een boom niet langer een boom is maar een boom bestaat uit gezondheid, natuur, zuurstof en alle andere associaties die wij als mens ontwikkelen bij het beeld van een boom. Ten slotte is er „the real”, de daadwerkelijke wereld, het 

<span class="cycle" level-1="fysieke" level-2="lijfelijk" level-3="lichamelijk" level-4="<a href=/?page=reality>TEST2</a>" >fysieke</span>
	
, het lichaam en de natuur. Al deze elementen zijn volgens Lacan niet uit te drukken in woorden omdat woorden „the symbolic” naar voren halen en onderscheid creëren waar deze er niet is. Maar als men niet in staat is het echte te 
	
<span class="cycle" level-1="bevatten" level-2="behelzen" level-3="doorzien" level-4="<a href=/?page=reality>TEST2</a>" >bevatten</span>
	
, omschrijven en erover te reflecteren, bestaat er in de menselijke bevatting dan wel een echte wereld?<br><br>
Slavoj Zizek gelooft van niet, hij is van mening dat we in onze 
	
<span class="cycle" level-1="hedendaagse" level-2="moderne" level-3="contemporaine" level-4="<a href=/?page=reality>TEST2</a>" >hedendaagse</span> 
	
maatschappij enkel en alleen nog maar in een simulatie leven, dat we geen realiteit meer hebben maar onze realiteit enkel en alleen bestaat uit beelden en 
	
<span class="cycle" level-1="symboliseringen" level-2="tekening" level-3="vertegenwoordigingen" level-4="<a href=/?page=reality>TEST2</a>" >symboliseringen</span>. 
	
Hij zegt hierover: <i>„Are the pessimistic cultural criticists (from Jean Baudrillard to Paul Virilio) justified in their claim that cyberspace ultimately 
	
<span class="cycle" level-1="generates" level-2="develop" level-3="set up" level-4="<a href=/?page=reality>TEST2</a>" >generates</span> 
	
a kind of proto-psychotic immersion into an imaginary universe of hallucinations, unconstrained by any symbolic Law or by any impossibility of some Real? If not, how are we to detect in cyberspace the contours of the other two dimensions of the Lacanian triad ISR, the Symbolic and the Real?” <sup>3</sup></i> Zizek stelt dat we een set parameters moeten stellen om de grenzen van de realiteit duidelijk te markeren alvorens ons over te geven aan een virtuele realiteit van media en 
	
<span class="cycle" level-1="symboliek" level-2="vertegenwoordiging" level-3="<a href=/?page=reality>TEST2</a>" >symboliek</span>
	
. <br>
<br>
Zoals Alfredo Jaar in zijn werk de duidelijke distinctie maakt tussen het beeld van Kevin Carter en de context van de situatie stelt hij ook een grens tussen het 

<span class="cycle" level-1="visuele" level-2="optische" level-3="zienbaar" level-4="<a href=/?page=reality>TEST2</a>" >visuele</span> 
	
en de tekstuele betekenis van deze twee media, het beeld is het middelpunt van het werk, en de aanleiding van het verhaal. De tekst is de toelichting en bevrijding van het beeld door middel van 

<span class="cycle" level-1="subtiele" level-2="kleine" level-3="onopvallende" level-4="<a href=/?page=reality>TEST2</a>" >subtiele</span> 
	
realiteit en zonder het gebruik van visuele stimulans. Jaar is zich erg bewust van de vorming van onze realiteit onder de invloed van de hedendaagse visuele media, het werk lijkt zich te richten op deze 
	
<span class="cycle" level-1="perversie" level-2="corruptie" level-3="immoraliteit" level-4="<a href=/?page=reality>TEST2</a>" >perversie</span> 
	
van een realiteit en bevraagt de rol van de media in de vorming van ons wereldbeeld. <br>
<br>
Het feit dat Jaar deze 2 elementen splitst is interessant, gezien het feit dat tekst ook een medium is en volgens Lacan duidelijk ook bestaat uit tekens, symbolen en 

<span class="cycle" level-1="verwijzingen" level-2="referenties" level-3="betrekkingen" level-4="<a href=/?page=reality>TEST2</a>" >verwijzingen</span>. 
	
Jaar lijkt zich hierin dus meer te richten op de aard van de fotografie ten opzichte van de tekst en geeft de tekst daarin een hele andere rol. <i>„‘the medium is the message’ (McLuhan 1994: 7), the catchphrase that implies how new forms of media (rather than content) change the 
	
<span class="cycle" level-1="perception" level-2="impression" level-3="sense" level-4="<a href=/?page=reality>TEST2</a>" >perception</span> 
	
of society, and move from a linear way of understanding the world (first oral, then written narratives) to a constant and contradictory flux of signs (which are seen and 
	
<span class="cycle" level-1="interpreted" level-2="explained" level-3="understood" level-4="<a href=/?page=reality>TEST2</a>" >interpreted</span>
	
).” <sup>4</sup></i> Als het gaat om een discrepantie tussen een lineaire en een non-lineaire denkrichting is de keuze van een film op deze wijze interessant. Jaar laat de foto tussen de teksten door verschijnen in de 
	
<span class="cycle" level-1="chronologische" level-2="sequentiele" level-3="<a href=/?page=reality>TEST2</a>" >chronologische</span> 
	
	volgorde van het verhaal en geeft de foto daarmee in zekere zin de rol die een tekst zou hebben in de lineaire manier van <span class="cycle" level-1="analyseren" level-2="evalueren" level-3="inspecteren" level-4="<a href=/?page=reality>TEST2</a>" >analyseren</span>
	
. Een subtiel statement om nogmaals het verschil in impact dat verschillende media op onze realiteit hebben te illustreren. <br>
<br>
De ontwikkeling van media zoals het fotografische en cinematografische beeld zijn belangrijke 

<span class="cycle" level-1="ontwikkelingen" level-2="progressie" level-3="veranderingen" level-4="<a href=/?page=reality>TEST2</a>" >ontwikkelingen</span>
	
in de perceptie van de wereld door de 
	
<span class="cycle" level-1="mens" level-2="homosapien" level-3="<a href=/?page=reality>TEST2</a>" >mens</span>
	
. Door deze steeds realistischere wijzen van weergeven en dupliceren wordt het moeilijk om nog onderscheid te maken tussen de realiteit en de representatie van realiteit. Men kan echter beargumenteren: als een duplicaat een object is met de exacte eigenschappen van het 

<span class="cycle" level-1="origineel" level-2="oorspronkelijke" level-3="authentieke" level-4="<a href=/?page=reality>TEST2</a>" >origineel</span>
	
, wat onderscheidt het dan nog, en is het nog van belang om überhaupt onderscheiden te worden? Misschien moeten we ons overgeven, onszelf laten verzwelgen in de wereld van simulaties, media, signalen en reproducties. Tenslotte, als deze wereld een exacte 
	
<span class="cycle" level-1="replica" level-2="verdubbeling" level-3="kopie" level-4="<a href=/?page=reality>TEST2</a>" >replica</span> 
	
is van de wereld waar we ons in begeven, wie merkt dan nog het verschil?
	
